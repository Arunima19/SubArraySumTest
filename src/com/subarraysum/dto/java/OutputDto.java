package com.subarraysum.dto.java;

public class OutputDto {
	private Integer maxSum;
	private Integer startingIndex;
	private Integer endingIndex;
	private Integer length;
	private Integer[] elements;
	public Integer getMaxSum() {
		return maxSum;
	}
	public void setMaxSum(Integer maxSum) {
		this.maxSum = maxSum;
	}
	public Integer getStartingIndex() {
		return startingIndex;
	}
	public void setStartingIndex(Integer startingIndex) {
		this.startingIndex = startingIndex;
	}
	public Integer getEndingIndex() {
		return endingIndex;
	}
	public void setEndingIndex(Integer endingIndex) {
		this.endingIndex = endingIndex;
	}
	public Integer getLength() {
		return length;
	}
	public void setLength(Integer length) {
		this.length = length;
	}
	public Integer[] getElements() {
		return elements;
	}
	public void setElements(Integer[] elements) {
		this.elements = elements;
	}
	
	
}
